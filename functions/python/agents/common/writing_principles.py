# ── 프롬프트 빌더용 공개 함수 ────────────────────────────────

def build_writing_principles_xml() -> str:
    """범용 긍정 글쓰기 원칙을 XML 블록으로 반환.

    fingerprint/generationProfile과 무관하게 **모든 사용자**에게 상시 주입된다.
    """
    parts: list[str] = [
        '<writing_principles priority="high">',
        '  <overview>아래는 좋은 정치 글쓰기의 기본 원칙이다. 모�� 원고에서 따를 것.</overview>',
    ]

    for p in _PRINCIPLES:
        lines = [f'  <principle id="{p["id"]}">']
        lines.append(f'    <rule>{_xml_text(p["rule"])}</rule>')
        if p.get("bad"):
            lines.append(f'    <bad>{_xml_text(p["bad"])}</bad>')
        if p.get("good"):
            lines.append(f'    <good>{_xml_text(p["good"])}</good>')
        if p.get("good_a"):
            lines.append(f'    <good>{_xml_text(p["good_a"])}</good>')
        if p.get("good_b"):
            lines.append(f'    <good>{_xml_text(p["good_b"])}</good>')
        lines.append('  </principle>')
        parts.append("\n".join(lines))

    parts.append('</writing_principles>')
    return "\n".join(parts)
