# 브랜드명 → 지자체 매핑
LOCAL_CURRENCY_TO_REGION: Dict[str, str] = {
    name: region for name, region in LOCAL_CURRENCY_ENTRIES
}


def get_kiwi_user_words() -> List[Tuple[str, str, float]]:
    """Kiwi add_user_word 에 전달할 (word, tag, score) 목록을 반환한다.

    - tag: 'NNP' (고유명사)
    - score: 0.0 (기본 가중치 — Kiwi 내부 사전과 동등 우선순위)
    """
    results: List[Tuple[str, str, float]] = []
    for name, _ in LOCAL_CURRENCY_ENTRIES:
        # 순수 한글이 아닌 토큰(e음, 1004섬 등)도 등록 — Kiwi 가 분절하지
        # 않고 한 덩어리로 인식하게 한다.
        if len(name) >= 2:
            results.append((name, "NNP", 0.0))
    return results
